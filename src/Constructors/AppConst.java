package Constructors;

/**
 *
 * @author camilo.soler
 */
public class AppConst {
    private int id;
    private String nombre;
    private int type;
    private String url;
    private String path;

    public AppConst(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public AppConst(String nombre, int type, String url) {
        this.nombre = nombre;
        this.type = type;
        this.url = url;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    
    public AppConst(String nombre) {
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
}
